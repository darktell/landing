// Функция для рендеринга карточек
function renderCards(tabName) {
    const contentContainer = document.getElementById('tab-content');
    contentContainer.innerHTML = '';

    if (tabsData[tabName]) {
        tabsData[tabName].forEach(card => {
            const cardElement = document.createElement('div');
            cardElement.classList.add('card');

            cardElement.innerHTML = getTabCart(card);

            contentContainer.appendChild(cardElement);
        });
    }
}

function switchTab(event) {
    const tabName = event.target.dataset.tab;

    const tabs = document.querySelectorAll('.tab');
    tabs.forEach(tab => tab.classList.remove('active'));
    event.target.classList.add('active');

    renderCards(tabName);
}

document.querySelectorAll('.tab').forEach(tab => {
    tab.addEventListener('click', switchTab);
});

// render all as default
renderCards('all');
document.querySelector('.tab[data-tab="all"]').classList.add('active');